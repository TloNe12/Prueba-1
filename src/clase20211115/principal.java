    private void jmi_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmi_agregarActionPerformed
       String n,d;
       int t;
       n=JOptionPane.showInputDialog("nombre");
       d=JOptionPane.showInputDialog("direccion");
       t=Integer.parseInt(JOptionPane.showInputDialog("telefono"));
       contacto.add(new Contacto(n,t,d));
    }//GEN-LAST:event_jmi_agregarActionPerformed

