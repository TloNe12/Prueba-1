    private void bt_ingresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_ingresarMouseClicked
        // TODO add your handling code here:
        if (tf_user.getText().equals(u) && tf_pass.getText().equals(p)) {
            tf_user.setText("");
            tf_pass.setText("");
            jd_login.setVisible(false);
            jmi_login.setEnabled(false);
            jmi_contactos.setEnabled(true);
            jmi_guardar.setEnabled(true);
            jmi_logout.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(jd_login, "el usuario no existe");
            
        }
        
    }//GEN-LAST:event_bt_ingresarMouseClicked

